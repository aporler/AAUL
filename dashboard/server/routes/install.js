/**
 * Public installer endpoint.
 *
 * This route is intentionally self-contained because it has to generate a
 * complete installer script for Linux, macOS, or Windows from a one-time
 * install token. It is also one of the few routes that is expected to be
 * called before an agent exists as a running service.
 */
import express from "express";
import config from "../config.js";
import { dbOps } from "../db/index.js";
import { resolveBaseUrl } from "../utils/base-url.js";
import { getSetting } from "../utils/settings.js";
import { decryptSecret, generateToken, hashToken, protectToken } from "../utils/tokens.js";

const router = express.Router();

function isHttps(req) {
  return req.secure === true;
}

// Quote a value for POSIX shell scripts generated by this route.
function shellQuote(value) {
  return `'${String(value ?? "").replace(/'/g, `'\"'\"'`)}'`;
}

// Quote a value for PowerShell scripts generated by this route.
function psQuote(value) {
  return `'${String(value ?? "").replace(/'/g, "''")}'`;
}

// Keep schedule values predictable because they are embedded in generated scripts.
function sanitizeScheduleTime(value) {
  const raw = String(value || "");
  return /^([01]\d|2[0-3]):[0-5]\d$/.test(raw) ? raw : "03:00";
}

// Poll interval can come from DB settings or config. Normalize it before use.
function sanitizePollSeconds(value, fallback) {
  const parsed = Number.parseInt(value, 10);
  if (Number.isFinite(parsed) && parsed > 0) {
    return parsed;
  }
  const fallbackParsed = Number.parseInt(fallback, 10);
  return Number.isFinite(fallbackParsed) && fallbackParsed > 0 ? fallbackParsed : 15;
}

// Some installations may still be in a migration window. Fail closed here.
function extractAgentApiToken(agent) {
  try {
    return agent.agent_api_token_enc ? decryptSecret(agent.agent_api_token_enc) : agent.agent_api_token;
  } catch {
    return null;
  }
}

// Once a token is used successfully we persist it in the protected format only.
async function persistInstallToken(agent, installToken) {
  const protectedInstallToken = protectToken(installToken);
  await dbOps.run(
    "UPDATE agents SET install_token = NULL, install_token_hash = ?, install_token_enc = ? WHERE id = ?",
    [protectedInstallToken.hash, protectedInstallToken.encrypted, agent.id]
  );
  agent.install_token = null;
  agent.install_token_hash = protectedInstallToken.hash;
  agent.install_token_enc = protectedInstallToken.encrypted;
}

// If an agent has never connected yet we can safely rotate its API token here.
async function ensureInstallableAgentApiToken(agent) {
  const existingToken = extractAgentApiToken(agent);
  if (existingToken) {
    return existingToken;
  }
  if (agent.last_seen_at) {
    return null;
  }

  const agentApiToken = generateToken(32);
  const protectedAgentApiToken = protectToken(agentApiToken);
  await dbOps.run(
    "UPDATE agents SET agent_api_token = NULL, agent_api_token_hash = ?, agent_api_token_enc = ? WHERE id = ?",
    [protectedAgentApiToken.hash, protectedAgentApiToken.encrypted, agent.id]
  );
  agent.agent_api_token = null;
  agent.agent_api_token_hash = protectedAgentApiToken.hash;
  agent.agent_api_token_enc = protectedAgentApiToken.encrypted;
  return agentApiToken;
}

// Shared shell helpers injected into both Linux and macOS installer templates.
function buildShellBundleVerificationHelpers() {
  return `header_value() {
  key="$1"
  tr -d '\\r' < "$TMP_HEADERS" | awk -F': ' -v wanted="$key" 'tolower($1)==tolower(wanted){print $2}' | tail -n 1
}

python_sha256() {
  "\${PYTHON_BIN}" - "$1" <<'PY'
import hashlib
import sys

digest = hashlib.sha256()
with open(sys.argv[1], "rb") as handle:
    while True:
        chunk = handle.read(1024 * 1024)
        if not chunk:
            break
        digest.update(chunk)
print(digest.hexdigest())
PY
}

python_hmac() {
  "\${PYTHON_BIN}" - "$1" "$2" <<'PY'
import hashlib
import hmac
import sys

print(hmac.new(sys.argv[2].encode("utf-8"), sys.argv[1].encode("utf-8"), hashlib.sha256).hexdigest())
PY
}

download_bundle() {
  curl -fsSLo "$TMP_BUNDLE" -D "$TMP_HEADERS" \\
    -H "Authorization: Bearer $AGENT_API_TOKEN" \\
    "$DASHBOARD_URL/agent/latest.tar.gz"
}

verify_bundle() {
  expected_sha="$(header_value X-Bundle-Sha256)"
  expected_sig="$(header_value X-Bundle-Signature)"
  if [ -z "$expected_sha" ] || [ -z "$expected_sig" ]; then
    echo "Missing bundle integrity headers." >&2
    exit 1
  fi

  actual_sha="$(python_sha256 "$TMP_BUNDLE")"
  if [ "$actual_sha" != "$expected_sha" ]; then
    echo "Bundle checksum mismatch." >&2
    exit 1
  fi

  actual_sig="$(python_hmac "$expected_sha" "$AGENT_API_TOKEN")"
  if [ "$actual_sig" != "$expected_sig" ]; then
    echo "Bundle signature mismatch." >&2
    exit 1
  fi
}
`;
}

// Linux installer: install prerequisites, fetch the bundle, write config, and wire services.
function buildLinuxScript(agent, baseUrl, agentApiToken, scheduleEnabled, scheduleTime, pollSeconds) {
  return `#!/bin/sh
set -eu

DASHBOARD_URL=${shellQuote(baseUrl)}
AGENT_ID=${shellQuote(agent.id)}
DISPLAY_NAME=${shellQuote(agent.display_name)}
AGENT_API_TOKEN=${shellQuote(agentApiToken)}
POLL_SECONDS=${pollSeconds}
SCHEDULE_ENABLED=${scheduleEnabled}
SCHEDULE_TIME=${shellQuote(scheduleTime)}

INSTALL_DIR="/opt/agentautoupdate"
APP_DIR="$INSTALL_DIR/app"
LOG_DIR="$INSTALL_DIR/logs"
VENV_DIR="$INSTALL_DIR/venv"
CLI_PATH="/usr/local/bin/agentautoupdate"
TMP_BUNDLE="/tmp/agentautoupdate-latest.tar.gz"
TMP_HEADERS="/tmp/agentautoupdate-latest.headers"
PYTHON_BIN=""

mkdir -p "$INSTALL_DIR" "$LOG_DIR"
chmod 700 "$INSTALL_DIR" "$LOG_DIR"

install_requirements() {
  if command -v apt-get >/dev/null 2>&1; then
    apt-get update -y
    apt-get install -y python3 python3-venv python3-pip curl ca-certificates openssl tar
  elif command -v dnf >/dev/null 2>&1; then
    dnf -y install python3 python3-pip python3-virtualenv curl ca-certificates openssl tar
  elif command -v yum >/dev/null 2>&1; then
    yum -y install python3 python3-pip python3-virtualenv curl ca-certificates openssl tar
  elif command -v pacman >/dev/null 2>&1; then
    pacman -Sy --noconfirm --needed python python-pip curl ca-certificates openssl tar
  else
    echo "Unsupported distribution: apt, dnf, yum or pacman required." >&2
    exit 1
  fi
}

detect_python() {
  if command -v python3 >/dev/null 2>&1; then
    PYTHON_BIN="$(command -v python3)"
  elif command -v python >/dev/null 2>&1; then
    PYTHON_BIN="$(command -v python)"
  else
    echo "Python 3 is required." >&2
    exit 1
  fi
}

${buildShellBundleVerificationHelpers()}

install_requirements
detect_python
"$PYTHON_BIN" -m venv "$VENV_DIR"
"$VENV_DIR/bin/pip" install --upgrade pip --quiet

download_bundle
verify_bundle

rm -rf "$APP_DIR"
mkdir -p "$APP_DIR"
tar -xzf "$TMP_BUNDLE" -C "$APP_DIR"
"$VENV_DIR/bin/pip" install -r "$APP_DIR/requirements.txt" --quiet

umask 077
cat > "$INSTALL_DIR/config.json" <<JSON
{
  "agentId": "$AGENT_ID",
  "displayName": "$DISPLAY_NAME",
  "dashboardUrl": "$DASHBOARD_URL",
  "agentApiToken": "$AGENT_API_TOKEN",
  "pollIntervalSeconds": $POLL_SECONDS,
  "schedule": {
    "enabled": ${scheduleEnabled},
    "dailyTime": "$SCHEDULE_TIME"
  }
}
JSON

cat > "$CLI_PATH" <<'EOF'
#!/bin/sh
exec /opt/agentautoupdate/venv/bin/python /opt/agentautoupdate/app/agent_cli.py "$@"
EOF
chmod 755 "$CLI_PATH"

cat > /etc/systemd/system/agentautoupdate.service <<'EOF'
[Unit]
Description=Agent Auto Update Poller
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
User=root
Group=root
WorkingDirectory=/opt/agentautoupdate
ExecStart=/opt/agentautoupdate/venv/bin/python /opt/agentautoupdate/app/agent_poller.py
Restart=always
RestartSec=5
Environment=PYTHONUNBUFFERED=1
UMask=0077

[Install]
WantedBy=multi-user.target
EOF

cat > /etc/systemd/system/agentautoupdate-run.service <<'EOF'
[Unit]
Description=Agent Auto Update Runner
After=network-online.target
Wants=network-online.target

[Service]
Type=oneshot
User=root
Group=root
WorkingDirectory=/opt/agentautoupdate
ExecStart=/opt/agentautoupdate/venv/bin/python /opt/agentautoupdate/app/agent_runner.py --run-once
Environment=PYTHONUNBUFFERED=1
UMask=0077
EOF

cat > /etc/systemd/system/agentautoupdate-run.timer <<EOF
[Unit]
Description=Agent Auto Update Schedule

[Timer]
OnCalendar=*-*-* $SCHEDULE_TIME:00
Persistent=true
Unit=agentautoupdate-run.service

[Install]
WantedBy=timers.target
EOF

systemctl daemon-reload
systemctl enable --now agentautoupdate.service

if [ "$SCHEDULE_ENABLED" = "1" ]; then
  systemctl enable --now agentautoupdate-run.timer
else
  systemctl disable --now agentautoupdate-run.timer || true
fi

"$VENV_DIR/bin/python" "$APP_DIR/agent_poller.py" --once || true
rm -f "$TMP_BUNDLE" "$TMP_HEADERS"

echo "Agent installed for Linux."
echo "CLI:    $CLI_PATH"
echo "Status: systemctl status agentautoupdate.service"
`;
}

